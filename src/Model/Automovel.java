package Model;

public class Automovel {
    String matricula;
    int ano;
    String marca;
    String modelo;
    String placa;
    String registro;

    Automovel(String matricula, int ano, String marca, String modelo, String placa, String registro){
        this.matricula = matricula;
        this.ano = ano;
        this.marca = marca;
        this.modelo = modelo;
        this.placa = placa;
        this.registro = registro;
    }
}
